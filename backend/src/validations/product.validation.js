const Joi = require('joi')

const createProductSchema = Joi.object({
	medicineCode: Joi.string().trim().required(),
	categoryId: Joi.string().trim().required(),
	categoryName: Joi.string().trim().required(),
	productName: Joi.string().trim().required(),
	price: Joi.number().min(0).required(),
	usageSummary: Joi.string().allow('').default(''),
	mainIngredients: Joi.string().allow('').default(''),
	targetUsers: Joi.string().allow('').default(''),
	brand: Joi.string().allow('').default(''),
	manufacturer: Joi.string().allow('').default(''),
	ingredients: Joi.string().allow('').default(''),
	usage: Joi.string().allow('').default(''),
	dosage: Joi.string().allow('').default(''),
	contraindications: Joi.string().allow('').default(''),
	sideEffects: Joi.string().allow('').default(''),
	precautions: Joi.string().allow('').default(''),
	pharmacology: Joi.string().allow('').default(''),
	additionalInfo: Joi.string().allow('').default(''),
	storage: Joi.string().allow('').default(''),
	packaging: Joi.string().allow('').default(''),
	expiry: Joi.string().allow('').default(''),
	manufacturerDetail: Joi.string().allow('').default(''),
	characteristics: Joi.string().allow('').default(''),
	images: Joi.string().allow('').default(''),
	activeIngredient: Joi.string().allow(''),
	medicineName: Joi.string().allow(''),
	unit: Joi.string().allow(''),
	description: Joi.string().allow(''),
	requiresPrescription: Joi.boolean().default(false),
	isActive: Joi.boolean().default(true),
})

const updateProductSchema = Joi.object({
	medicineCode: Joi.string().trim(),
	categoryId: Joi.string().trim(),
	categoryName: Joi.string().trim(),
	productName: Joi.string().trim(),
	price: Joi.number().min(0),
	usageSummary: Joi.string().allow(''),
	mainIngredients: Joi.string().allow(''),
	targetUsers: Joi.string().allow(''),
	brand: Joi.string().allow(''),
	manufacturer: Joi.string().allow(''),
	ingredients: Joi.string().allow(''),
	usage: Joi.string().allow(''),
	dosage: Joi.string().allow(''),
	contraindications: Joi.string().allow(''),
	sideEffects: Joi.string().allow(''),
	precautions: Joi.string().allow(''),
	pharmacology: Joi.string().allow(''),
	additionalInfo: Joi.string().allow(''),
	storage: Joi.string().allow(''),
	packaging: Joi.string().allow(''),
	expiry: Joi.string().allow(''),
	manufacturerDetail: Joi.string().allow(''),
	characteristics: Joi.string().allow(''),
	images: Joi.string().allow(''),
	activeIngredient: Joi.string().allow(''),
	medicineName: Joi.string().allow(''),
	unit: Joi.string().allow(''),
	description: Joi.string().allow(''),
	requiresPrescription: Joi.boolean(),
	isActive: Joi.boolean(),
}).min(1)

const addInventoryBatchSchema = Joi.object({
	batchNumber: Joi.string().trim().required(),
	quantity: Joi.number().integer().min(0).required(),
	expiryDate: Joi.date().iso().required(),
	importPrice: Joi.number().min(0).required(),
})

const productQuerySchema = Joi.object({
	page: Joi.number().integer().min(1).default(1),
	limit: Joi.alternatives().try(
		Joi.number().integer().min(0).max(100),
		Joi.string().valid('all', '0'),
	).default(20),
	categoryId: Joi.string().trim(),
	search: Joi.string().trim().max(200),
	sortBy: Joi.string().valid('price_asc', 'price_desc', 'newest', 'name').default('newest'),
	priceMin: Joi.number().min(0),
	priceMax: Joi.number().min(0),
	status: Joi.string().valid('active', 'inactive', 'all').default('active'),
})

module.exports = {
	createProductSchema,
	updateProductSchema,
	addInventoryBatchSchema,
	productQuerySchema,
}
